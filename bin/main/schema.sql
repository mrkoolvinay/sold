DROP TABLE IF EXISTS ITEM;
DROP TABLE IF EXISTS AUCTION;
DROP TABLE IF EXISTS SELLER;
DROP TABLE IF EXISTS BUYER;
DROP TABLE IF EXISTS BIDS;

CREATE TABLE ITEM (
id INT AUTO_INCREMENT  PRIMARY KEY,
NAME VARCHAR(100),
PRICE DECIMAL(10, 2)
);

CREATE TABLE AUCTION (
id INT AUTO_INCREMENT  PRIMARY KEY,
SELLERID INT,
ITEMID INT,
LOWBID DECIMAL(10,2),
HIGHBID DECIMAL(10,2),
PARTCHARGES DECIMAL(10,2),
BUYERID INT,
SOLDPRICE DECIMAL(10,2)
);

CREATE TABLE SELLER (
id INT AUTO_INCREMENT  PRIMARY KEY,
NAME VARCHAR(100)
);

CREATE TABLE BUYER(
id INT AUTO_INCREMENT  PRIMARY KEY,
NAME VARCHAR(100)
);

CREATE TABLE BIDS (
id INT AUTO_INCREMENT  PRIMARY KEY,
BUYERID INT,
AUCTIONID INT,
BIDPRICE DECIMAL(10,2)
);